<strong> ¿Cómo Comprar en Nuestra Tienda Virtual?</strong> <br>
Comprar en nuestra tienda es fácil, rápido y seguro. A continuación, te explicamos paso a paso todo lo que necesitás saber para realizar tu compra online.<br><br>

<strong>1. Registrate o Iniciá Sesión </strong> <br>
Para realizar una compra necesitás estar registrado. Es muy sencillo: <br>

Hacé clic en el botón "Mi cuenta" y luego en “Registrarse” en la parte superior  derecha de la página. <br>

Completá tus datos (nombre, correo electrónico, contraseña). <br>

¡Listo! Ya podés iniciar sesión y comenzar a comprar. <br>

Si ya tenés cuenta, simplemente iniciá sesión con tu usuario y contraseña. <br> <br>

<strong> 2. Elegí tus Productos </strong> <br> 
Navegá por nuestras categorías o usá el buscador para encontrar lo que necesitás. <br>

Hacé clic en el producto que te interesa para ver más detalles. <br>

Seleccioná la cantidad deseada y hacé clic en “Agregar al carrito”. <br>

Podés seguir navegando y agregando más productos antes de finalizar tu compra. <br> <br>

<strong> 3. Medios de Pago </strong> <br>
Ofrecemos múltiples opciones seguras para que elijas la que más te convenga:<br>

Tarjetas de crédito y débito (Visa, MasterCard, American Express, etc.)<br>

Transferencia bancaria<br>

Pago en efectivo a través de Rapipago / Pago Fácil (con código QR o cupón)<br>

Mercado Pago<br>

Todas las transacciones están protegidas por protocolos de seguridad para garantizar tu privacidad.<br> <br>

<strong> 4. Medios de Envío </strong> <br>
Enviamos a todo el país. Podés elegir entre las siguientes opciones: <br>

Envío a domicilio (por correo privado o transporte) <br>

Retiro en sucursal (según disponibilidad en tu ciudad) <br><br>

<strong> 5. Finalizá tu Compra </strong> <br>
Revisá tu carrito y hacé clic en “Finalizar compra”. <br>

Ingresá tus datos de envío y seleccioná el medio de pago. <br>

Confirmá tu pedido. Te enviaremos un correo con los detalles de tu compra y el seguimiento del envío. <br> <br>

<strong> ¿Dudas? </strong>  <br> Nuestro equipo de atención al cliente está disponible para ayudarte. Escribinos por WhatsApp, redes sociales o a través de nuestro formulario de contacto. 
